package datastructures.object_oriented_design.Q8_3_MusicJukeBox;

public class Artiste {
    private String artisteName;
    private int age;
    /**
     * @return the artisteName
     */
    public String getArtisteName() {
	return artisteName;
    }
    /**
     * @param artisteName the artisteName to set
     */
    public void setArtisteName(String artisteName) {
	this.artisteName = artisteName;
    }
    /**
     * @return the age
     */
    public int getAge() {
	return age;
    }
    /**
     * @param age the age to set
     */
    public void setAge(int age) {
	this.age = age;
    }
}
